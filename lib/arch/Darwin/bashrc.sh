# On macOS, the default shell is zsh, so we need to set the 
# BASH_SILENCE_DEPRECATION_WARNING variable to 1 to suppress the deprecation 
# warning when using bash.
export BASH_SILENCE_DEPRECATION_WARNING=1

# Homebrew is a package manager for macOS and Linux. It is used to install and 
# manage software packages. The HOMEBREW_NO_AUTO_UPDATE environment variable is 
# used to disable automatic updates for Homebrew when running commands.
export HOMEBREW_NO_AUTO_UPDATE=1

